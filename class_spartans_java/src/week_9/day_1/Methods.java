public class Methods {

     /*
    Method Design - Real-Life Analogy:

    Imagine you're at a coffee shop. You place an order for a cappuccino. The barista follows a
    specific set of steps (or a "recipe") to make that cappuccino: steam the milk, brew the espresso,
     pour the espresso into the cup, add the steamed milk, and finish it off with a sprinkle
     of cocoa or cinnamon if you desire. This standard procedure or recipe to make a cappuccino
     can be likened to a method in programming. Just like you might order a cappuccino with specific
      customizations (e.g., almond milk, an extra shot of espresso), methods in programming can take
      parameters to produce slightly varied outputs based on the inputs.

    Method Design - Programming Explanation:

    In programming, a method is a block of code within a class that performs a specific task.
    Methods are essential for the concept of encapsulation in object-oriented design.
    They provide a way to access and manipulate the data of an object.

    we have two types of methods in general:

    1: user defined methods (functions): These methods or function are called custom
    methods in which user is going to define them!

    2: Build in methods (functions): they're already created for us and ready for use.


Key points about method design:

	1:	Name: A method should have a descriptive name that indicates its purpose.
	It's a common practice to use verb-noun
	pairs for method names, e.g., calculateArea(), printDetails(), etc.

	2:	Parameters: Methods can take input values known as parameters. These allow methods
	to perform operations using different input values.

	3:	Return Type: A method can return a value. The data type of this return value
	(e.g., int, String, boolean) is specified in the method declaration. If a method doesn't
	 return any value, it's marked with the void keyword.

	4:	Access Modifiers: These determine the visibility of the method, i.e., where the method can be
	called from. Common modifiers include public, private, and protected.

	5:	Body: The set of statements that define what the method does.

*/


}
