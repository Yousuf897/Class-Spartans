package week_8.lab_session;

public class ReviewLoop {

    public static void main(String[] args) {

        // Loops --> Repeatedly executing the same block of code again and again.
        // If you want to repeatedly execute the same block of code again and again you use loops.


        // For Loop
        /*
        * Syntax of for loop
        *
        * for ( Initialization; Condition; Iteration ) {
        *   // Body of Loop
        * }
        * */

        // 1 2 3 4 5 . . . . . . 10
        // print John Doe 10 times
        for ( int iteration = 1; iteration <= 10; iteration++ ) {
            System.out.print(iteration + " ");
        }



    }

}
