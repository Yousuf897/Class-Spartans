package week_7.day_3;

public class ForLoopTestTwo {

    public static void main(String[] args) {

        // Print John 15 times!
        for ( int i = 1; i <= 15; i++ ) {
            System.out.print( i + ". John" + " " );
        }

        // 1. John
        // 2. John
        // 3. John

    }

}
