package week_13.day_2.interfacetopic;

public class WhatIsInterface {


    /*
    *
    * An interface in Java looks similar to a class but both the interface and class are two different concepts.
    *  An interface can have methods and variables just like the class but the methods
    * declared in interface are by default abstract.
    * We can achieve 100% abstraction and multiple inheritance in Java with Interface.

public interface Movable {
    int MAX_SPEED = 100;  // This is a constant

    void move();  // Abstract method

    default void stop() {
        System.out.println("Stopping");
    }

    static void staticMethod() {
        System.out.println("Static method in interface");
    }
}

public class Car implements Movable {
    @Override
    public void move() {
        System.out.println("Car is moving");
    }
}


By default all variables in Interface are final and static so you have to initialize them.

Why Do I need Inteface?
Can I implement multiple Intefaces?

    *
    * */

}
