package week_1.day_2;

public class WhyJavaInTesting {

    /*
    * Imagine you've built a toy car and now you want to make sure it can actually roll
    * without falling apart, or that its remote control works as expected. Testing is similar
    * in the world of software. You want to check that your code does what it's supposed to do under
    * various conditions.


Java plays a big role in the world of software testing, and here's how:


    1. JUnit: Think of JUnit like the "crash test" facility for your Java programs.
    * JUnit is a popular framework used for writing tests in Java.
    * It allows you to check that individual pieces of your code (like methods or classes)
    *  are working correctly.


    2. Selenium: Imagine being able to hire a robot to play with the toy car you built,
    * testing all the buttons and driving it around to make sure everything works well.
    * Selenium does something similar for web applications.  It can automate a web browser
    * to perform actions like clicking buttons, filling out forms, etc.,
    *  and it can be scripted using Java.

    3. Test Automation: Java's robustness and libraries make it
    *  a go-to language for automated testing. Imagine a factory production
    *  line where each toy car is automatically tested for defects.
     In a similar fashion, Java can help automate complex test scenarios for large software systems.

*
  Mocking: Sometimes, you want to test just one piece of your toy car, like its wheels,
  * without worrying about other parts like the remote control. In Java, you can use libraries
  *  like Mockito to "mock" parts of the system, so you can test individual components in isolation.


    Integration Testing: After testing the wheels, the remote control, and other individual parts,
    * you'd want to test the whole toy car together. Java also facilitates this kind of "integration
    * testing" where you test multiple components as a single unit.


    Load and Performance Testing: You'd also want to make sure your toy car can handle rough
    *  surfaces or go at high speeds without falling apart. Similarly, tools like Apache JMeter
    * can be used with Java to
    *
    simulate heavy loads on software applications to test their performance.
    * */

}
